// Define a constant named add and assign it an arrow function.
// This function takes two arguments, numOne and numTwo, both of type number, and returns a number.
const add = (numOne: number, numTwo: number): number => {
  // Add numOne and numTwo and return the result.
  return numOne + numTwo;
}

// Call the add function with the arguments 12 and 12, and log the result to the console.
console.log(add(12, 12));  // Output: 24
