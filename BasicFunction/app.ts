// Define a function named calculateProduct that takes two arguments, numOne and numTwo, both of type number,
// and returns a number.
function calculateProduct(numOne: number, numTwo: number): number {
  // Multiply numOne and numTwo and return the result.
  return numOne * numTwo;
}

// Call the calculateProduct function with the arguments 10 and 5, and log the result to the console.
console.log(calculateProduct(10, 5));
